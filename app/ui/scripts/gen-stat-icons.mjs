// Regenerate src/landing/statIconData.js from lucide-static.
//   node scripts/gen-stat-icons.mjs
// Bakes a curated, theme-grouped subset of real lucide icons into a checked-in
// data module so the static export carries no runtime lucide dependency, while
// every theme still has several distinct icons (no repeats in the icons
// treatment). Edit THEMES below to add/reorder icons, then re-run.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);
const NODES = require("lucide-static/icon-nodes.json");

// Theme → ordered icon candidates (best first). A stat matched to a theme takes
// the first candidate not already used in its section, so three "money" stats
// show three DIFFERENT money icons instead of three banknotes.
const THEMES = {
  money:     ["banknote", "coins", "piggy-bank", "wallet", "hand-coins", "credit-card", "receipt", "circle-dollar-sign", "badge-dollar-sign", "vault"],
  people:    ["users", "user-round", "users-round", "contact-round", "person-standing", "heart-handshake", "baby", "accessibility", "hand-heart"],
  education: ["graduation-cap", "book-open", "book", "library", "notebook-pen", "pencil", "backpack", "presentation", "school"],
  health:    ["heart-pulse", "stethoscope", "activity", "pill", "cross", "hospital", "syringe", "brain", "bandage"],
  climate:   ["leaf", "tree-deciduous", "tree-pine", "sprout", "sun", "cloud", "wind", "recycle", "flower-2", "mountain"],
  housing:   ["house", "building", "building-2", "warehouse", "door-open", "key-round", "hotel", "tent"],
  gov:       ["landmark", "scale", "gavel", "vote", "flag", "shield", "book-check", "stamp"],
  time:      ["hourglass", "clock", "calendar", "timer", "calendar-days", "watch", "alarm-clock", "history"],
  up:        ["trending-up", "arrow-up-right", "chart-line", "chart-column-increasing", "move-up-right", "chart-spline"],
  down:      ["trending-down", "arrow-down-right", "chart-column-decreasing", "move-down-right"],
  work:      ["briefcase", "hard-hat", "wrench", "hammer", "factory", "laptop", "handshake", "construction"],
  food:      ["wheat", "apple", "carrot", "utensils", "egg", "beef", "sandwich"],
  water:     ["droplet", "droplets", "sailboat", "glass-water", "cup-soda", "ship", "anchor"],
  global:    ["globe", "earth", "map", "map-pin", "plane", "languages", "network", "radar"],
  energy:    ["zap", "battery-full", "fuel", "plug", "lightbulb", "flame"],
  tech:      ["cpu", "database", "server", "wifi", "smartphone", "monitor", "code"],
  alert:     ["triangle-alert", "octagon-alert", "circle-alert", "shield-alert", "siren"],
  generic:   ["circle-dot", "hash", "target", "star", "chart-pie", "percent", "sparkle", "dot"],
};

const kept = {};
const data = {};
const missing = [];
const seen = new Set();
for (const [theme, names] of Object.entries(THEMES)) {
  kept[theme] = [];
  for (const n of names) {
    if (!NODES[n]) { missing.push(`${theme}:${n}`); continue; }
    kept[theme].push(n);
    if (!seen.has(n)) { seen.add(n); data[n] = NODES[n]; }
  }
}

const dataStr = "{\n" + Object.entries(data)
  .map(([n, nodes]) => `  ${JSON.stringify(n)}: ${JSON.stringify(nodes)},`).join("\n") + "\n}";
const themeStr = "{\n" + Object.entries(kept)
  .map(([t, ns]) => `  ${t}: ${JSON.stringify(ns)},`).join("\n") + "\n}";

const out = `// GENERATED by scripts/gen-stat-icons.mjs — do not edit by hand.
// Curated stat icons baked from lucide-static (icon-nodes.json), so the export
// stays self-contained (no runtime lucide dependency) while giving every theme
// several distinct icons to avoid repeats in the icons treatment.
export const ICON_DATA = ${dataStr};

export const THEME_ICONS = ${themeStr};

// a broad fallback order (all icons, de-duped) — when a theme's icons are all
// used in a section, we pull the next unused icon from here.
export const ICON_ORDER = ${JSON.stringify(Object.keys(data))};
`;

fs.writeFileSync(path.join(__dirname, "../src/landing/statIconData.js"), out);
console.log("icons kept:", Object.keys(data).length);
console.log("missing (dropped):", missing.length ? missing.join(", ") : "none");
